package solution;

import java.util.Scanner;

/**
 *
 * @author VINH
 */
public class Controller {

    public static void selectionSort(int[] a) {
        int len = a.length;
        for (int i = 0; i < len; i++) {
            int minIndex = i;
            for (int j = i + 1; j < len; j++) {
                if (a[j] < a[minIndex]) {
                    minIndex = j;
                }
            }
            if (minIndex != i) {
                int temp = a[minIndex];
                a[minIndex] = a[i];
                a[i] = temp;
            }
            if (i == 1) {
                break;
            }
        }
    }

}
